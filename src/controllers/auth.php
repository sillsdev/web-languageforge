    //redirect if needed, otherwise display the user list
    public function index()
    {

        if (!$this->ion_auth->logged_in()) {
            //redirect them to the login page
            redirect('/auth/login', 'refresh');
        } elseif (!$this->ion_auth->is_admin()) {
            //redirect them to the home page because they must be an administrator to view this
            redirect('/', 'refresh');
        } else {
            //set the flash data error message if there is one
            $this->data['message'] = (validation_errors()) ? validation_errors() : $this->session->flashdata('message');

            //list the users
            $this->data['users'] = $this->ion_auth->users()->result();
            foreach ($this->data['users'] as $k => $user) {
                $this->data['users'][$k]->groups = $this->ion_auth->get_users_groups($user->id)->result();
            }

            $this->renderPage('auth/index', $this->data);
        }
    }

    //log the user in
    public function login()
    {
        $this->data['title'] = "Login";

        //validate form input
        $this->form_validation->set_rules('identity', 'Identity', 'required');
        $this->form_validation->set_rules('password', 'Password', 'required');

        if ($this->form_validation->run() == true) {
            //check to see if the user is logging in
            //check for "remember me"
            $remember = (bool) $this->input->post('remember');

            // TODO: refactor this to use AuthHelper::login. IJH 2014-07
            if ($this->ion_auth->login($this->input->post('identity'), $this->input->post('password'), $remember)) {
                // successful login
                $this->session->set_flashdata('message', $this->ion_auth->messages());

                $website = Website::get();
                $user = new \models\UserModel((string) $this->session->userdata('user_id'));

                // Validate user is admin or has a role on the site.  Otherwise, redirect to logout
                if ( ($user->role == SystemRoles::SYSTEM_ADMIN) ||
                    ($user->siteRole->offsetExists($website->domain) &&
                    ($user->siteRole[$website->domain] != SiteRoles::NONE)) ) {
                    // set the project context to user's default project
                    $projectId = $user->getDefaultProjectId($website->domain);

                    if ($projectId) {
                        $this->session->set_userdata('projectId', $projectId);
                    }

                    $referer = $this->session->userdata('referer_url');
                    if ($referer && strpos($referer, "/app") !== false) {
                        $this->session->unset_userdata('referer_url');
                        redirect($referer, 'location');
                    } elseif ($projectId) {
                        $project = ProjectModel::getById($projectId);
                        $url = "/app/" . $project->appName . "/$projectId";
                        redirect($url, 'location');
                    } else {
                        redirect('/', 'location');
                    }
                } else {
                    //log out with error msg
                    $logout = $this->ion_auth->logout();
                    $this->session->set_flashdata('message', 'You are not authorized to use this site');
                    redirect('/auth/login', 'refresh'); //use redirects instead of loading views for compatibility with MY_Controller libraries
                }
            } else {
                //if the login was un-successful
                //redirect them back to the login page
                $this->session->set_flashdata('message', $this->ion_auth->errors());
                redirect('/auth/login', 'refresh'); //use redirects instead of loading views for compatibility with MY_Controller libraries
            }
        } else {
            if (!$this->session->userdata('came_from_logout')) {
                $referer = $this->input->server('HTTP_REFERER');
                if (strpos($referer, '/auth') === false) {
                    $this->session->set_userdata('referer_url', $this->input->server('HTTP_REFERER'));
                }
            }
            $this->session->unset_userdata('came_from_logout');

            //the user is not logging in so display the login page
            //set the flash data error message if there is one
            $this->data['message'] = (validation_errors()) ? validation_errors() : $this->session->flashdata('message');

            $this->data['identity'] = array('name' => 'identity',
                'id' => 'identity',
                'type' => 'text',
                'value' => $this->form_validation->set_value('identity'),
            );
            $this->data['password'] = array('name' => 'password',
                'id' => 'password',
                'type' => 'password',
            );

            //$this->data["page"] = "login";
            //$this->load->view("auth/container", $this->data);
            $this->renderPage('auth/login', $this->data);
        }
    }

    //log the user out
    public function logout()
    {
        $this->data['title'] = "Logout";

        //log the user out
        $logout = $this->ion_auth->logout();

        //redirect them to the login page
        $this->session->set_flashdata('message', $this->ion_auth->messages());
        $this->session->set_userdata('came_from_logout', true);
        redirect('/auth/login', 'location');
    }

    //change password
    public function change_password()
    {
        $this->form_validation->set_rules('old', $this->lang->line('change_password_validation_old_password_label'), 'required');
        $this->form_validation->set_rules('new', $this->lang->line('change_password_validation_new_password_label'), 'required|min_length[' . $this->config->item('min_password_length', 'ion_auth') . ']|max_length[' . $this->config->item('max_password_length', 'ion_auth') . ']|matches[new_confirm]');
        $this->form_validation->set_rules('new_confirm', $this->lang->line('change_password_validation_new_password_confirm_label'), 'required');

        if (!$this->ion_auth->logged_in()) {
            redirect('/auth/login', 'refresh');
        }

        $user = $this->ion_auth->user()->row();

        if ($this->form_validation->run() == false) {
            //display the form
            //set the flash data error message if there is one
            $this->data['message'] = (validation_errors()) ? validation_errors() : $this->session->flashdata('message');

            $this->data['min_password_length'] = $this->config->item('min_password_length', 'ion_auth');
            $this->data['old_password'] = array(
                'name' => 'old',
                'id'   => 'old',
                'type' => 'password',
            );
            $this->data['new_password'] = array(
                'name' => 'new',
                'id'   => 'new',
                'type' => 'password',
                'pattern' => '^.{'.$this->data['min_password_length'].'}.*$',
            );
            $this->data['new_password_confirm'] = array(
                'name' => 'new_confirm',
                'id'   => 'new_confirm',
                'type' => 'password',
                'pattern' => '^.{'.$this->data['min_password_length'].'}.*$',
            );
            $this->data['user_id'] = array(
                'name'  => 'user_id',
                'id'    => 'user_id',
                'type'  => 'hidden',
                'value' => $user->id,
            );

            //render
            $this->renderPage('auth/change_password', $this->data);
        } else {
            $identity = $this->session->userdata($this->config->item('identity', 'ion_auth'));

            $change = $this->ion_auth->change_password($identity, $this->input->post('old'), $this->input->post('new'));

            if ($change) {
                //if the password was successfully changed
                $this->session->set_flashdata('message', $this->ion_auth->messages());
                $this->logout();
            } else {
                $this->session->set_flashdata('message', $this->ion_auth->errors());
                redirect('/auth/change_password', 'refresh');
            }
        }
    }

    //forgot password
    public function forgot_password()
    {
        $this->form_validation->set_rules('email', $this->lang->line('forgot_password_validation_email_label'), 'required');
        if ($this->form_validation->run() == false) {
            //setup the input
            $this->data['email'] = array('name' => 'email',
                'id' => 'email',
            );

            if ( $this->config->item('identity', 'ion_auth') == 'username' ) {
                $this->data['identity_label'] = $this->lang->line('forgot_password_username_identity_label');
            } else {
                $this->data['identity_label'] = $this->lang->line('forgot_password_email_identity_label');
            }

            //set any errors and display the form
            $this->data['message'] = (validation_errors()) ? validation_errors() : $this->session->flashdata('message');
            $this->renderPage('auth/forgot_password', $this->data);
        } else {
            // get identity from username or email
            $config_tables = $this->config->item('tables', 'ion_auth');
            // RM - error for some reason: $identity = $this->mongo_db->where('email', $this->input->post('email'))->limit('1')->get($config_tables['users'])->row();
            if ( $this->config->item('identity', 'ion_auth') == 'username' ) {
                // IJH - error for some reason: $identity = $this->ion_auth->where('username', strtolower($this->input->post('email')))->users()->row();
                $result = $this->mongo_db->where('username', $this->input->post('email'))->limit('1')->get($config_tables['users']);
            } else {
                // IJH - error for some reason: $identity = $this->ion_auth->where('email', strtolower($this->input->post('email')))->users()->row();
                $result = $this->mongo_db->where('email', $this->input->post('email'))->limit('1')->get($config_tables['users']);
            }
            $identity = $result[0];
            if (empty($identity)) {
                $this->ion_auth->set_message('forgot_password_email_not_found');
                $this->session->set_flashdata('message', $this->ion_auth->messages());
                redirect("auth/forgot_password", 'refresh');
            }

            //run the forgotten password method to email an activation code to the user
            // RM - error "Trying to get member of non-object:" $forgotten = $this->ion_auth->forgotten_password($identity->{$this->config->item('identity', 'ion_auth')});
            $forgotten = $this->ion_auth->forgotten_password($identity[$this->config->item('identity', 'ion_auth')]);

            if ($forgotten) {
                //if there were no errors
                $this->session->set_flashdata('message', $this->ion_auth->messages());
                redirect("/auth/login", 'location'); //we should display a confirmation page here instead of the login page
            } else {
                $this->session->set_flashdata('message', $this->ion_auth->errors());
                redirect("/auth/forgot_password", 'location');
            }
        }
    }

    //reset password - final step for forgotten password
    public function reset_password($code = null)
    {
        if (!$code) {
            show_404($this->site);
        }

        $user = $this->ion_auth->forgotten_password_check($code);

        if ($user) {
            //if the code is valid then display the password reset form

            $this->form_validation->set_rules('new', $this->lang->line('reset_password_validation_new_password_label'), 'required|min_length[' . $this->config->item('min_password_length', 'ion_auth') . ']|max_length[' . $this->config->item('max_password_length', 'ion_auth') . ']|matches[new_confirm]');
            $this->form_validation->set_rules('new_confirm', $this->lang->line('reset_password_validation_new_password_confirm_label'), 'required');

            if ($this->form_validation->run() == false) {
                //display the form

                //set the flash data error message if there is one
                $this->data['message'] = (validation_errors()) ? validation_errors() : $this->session->flashdata('message');

                $this->data['min_password_length'] = $this->config->item('min_password_length', 'ion_auth');
                $this->data['new_password'] = array(
                    'name' => 'new',
                    'id'   => 'new',
                'type' => 'password',
                    'pattern' => '^.{'.$this->data['min_password_length'].'}.*$',
                );
                $this->data['new_password_confirm'] = array(
                    'name' => 'new_confirm',
                    'id'   => 'new_confirm',
                    'type' => 'password',
                    'pattern' => '^.{'.$this->data['min_password_length'].'}.*$',
                );
                $this->data['user_id'] = array(
                    'name'  => 'user_id',
                    'id'    => 'user_id',
                    'type'  => 'hidden',
                    'value' => $user->id,
                );
                $this->data['csrf'] = $this->_get_csrf_nonce();
                $this->data['code'] = $code;

                //render
                $this->renderPage('auth/reset_password', $this->data);
            } else {
                // do we have a valid request?
                if (/*$this->_valid_csrf_nonce() === FALSE ||*/$user->id != $this->input->post('user_id')) {

                    //something fishy might be up
                    $this->ion_auth->clear_forgotten_password_code($code);

                    show_error($this->lang->line('error_csrf'));

                } else {
                    // finally change the password
                    $identity = $user->{$this->config->item('identity', 'ion_auth')};

                    $change = $this->ion_auth->reset_password($identity, $this->input->post('new'));

                    if ($change) {
                        //if the password was successfully changed
                        $this->session->set_flashdata('message', $this->ion_auth->messages());
                        $this->logout();
                    } else {
                        $this->session->set_flashdata('message', $this->ion_auth->errors());
                        redirect('/auth/reset_password/' . $code, 'refresh');
                    }
                }
            }
        } else {
            //if the code is invalid then send them back to the forgot password page
            $this->session->set_flashdata('message', $this->ion_auth->errors());
            redirect("/auth/forgot_password", 'refresh');
        }
    }

    //activate the user
    public function activate($id, $code=false)
    {
        if ($code !== false) {
            $activation = $this->ion_auth->activate($id, $code);
        } elseif ($this->ion_auth->is_admin()) {
            $activation = $this->ion_auth->activate($id);
        }

        if ($activation) {
            //redirect them to the auth page
            $this->session->set_flashdata('message', $this->ion_auth->messages());
            redirect("/auth", 'refresh');
        } else {
            //redirect them to the forgot password page
            $this->session->set_flashdata('message', $this->ion_auth->errors());
            redirect("/auth/forgot_password", 'refresh');
        }
    }

    //deactivate the user
    public function deactivate($id = null)
    {
        $id = $this->config->item('use_mongodb', 'ion_auth') ? (string) $id : (int) $id;

        $this->load->library('form_validation');
        $this->form_validation->set_rules('confirm', $this->lang->line('deactivate_validation_confirm_label'), 'required');
        $this->form_validation->set_rules('id', $this->lang->line('deactivate_validation_user_id_label'), 'required|alpha_numeric');

        if ($this->form_validation->run() == FALSE) {
            // insert csrf check
            $this->data['csrf'] = $this->_get_csrf_nonce();
            $this->data['user'] = $this->ion_auth->user($id)->row();

            $this->renderPage('auth/deactivate_user', $this->data);
        } else {
            // do we really want to deactivate?
            if ($this->input->post('confirm') == 'yes') {
                // do we have a valid request?
                if ($this->_valid_csrf_nonce() === FALSE || $id != $this->input->post('id')) {
                    show_error($this->lang->line('error_csrf'));
                }

                // do we have the right userlevel?
                if ($this->ion_auth->logged_in() && $this->ion_auth->is_admin()) {
                    $this->ion_auth->deactivate($id);
                }
            }

            //redirect them back to the auth page
            redirect('/auth', 'refresh');
        }
    }

